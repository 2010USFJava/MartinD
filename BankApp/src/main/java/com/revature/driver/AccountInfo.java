package com.revature.driver;

public class AccountInfo {
	
	String name;
	String address;
	int phoneNumber;
	String emailAddress;
	int dateOfBirth;
	String accountType;
	int accountNumber;
	
	
	AccountInfo(String name, String address, int phoneNumber, String emailAddress, int dateOfBirth, String accountType) {
		this.name = name;
		this.address = address;
		this.phoneNumber = phoneNumber;
		this.emailAddress = emailAddress;
		this.dateOfBirth = dateOfBirth;
		this.accountType = accountType;
	}
	
	

}
